--[[
    Estruturas de Controle
        Estruturas de controle permitem manipular / controlar o fluxo do código.
        
        Fluxo padrão do código:
            Sequencial

        Outros fluxos:
            Por condições
            Redirecionamentos
            Loops / Ciclos

        Estruturas Condicionais
            Permitem executar um bloco de código apenas se uma condição for verdadeira.
                If - Else

        Estruturas de Repetição
            Permitem executar um bloco de código um determinado número de vezes se uma condição for verdadeira.
                Loop While

                Loop Repeat

                Loop For
