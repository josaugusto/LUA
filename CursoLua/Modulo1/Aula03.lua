--[[
    Operadores são símbolos que permitem realizar uma operação entre dois valores.
    
    Tipos de Operadores

        Operador de Atribuição

            =

        Operadores Aritméticos

            São operadores utilizados para calculos aritméticos entre dois valores. Exemplos:
                + (Adição)
                - (Subtração / Negativo quando é usado como operador unário)
                * (Multiplicação)
                / (Divisão)
                // (Divisão Inteira)
                % (Módulo / Resto da Divisão)
                ^ (Exponenciação)

        Operadores Relacionais
            >, <, >=, <=

        Operadores lógicos
            and, or, not

        Operador de Cumprimento
            #

        Operador de Concatenação (..)
            Serve para unir strings (Textos)
]]
